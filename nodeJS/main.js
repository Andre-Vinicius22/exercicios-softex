// executando atividade "manipulando_strings" com node.js

// transformando uma string minuscula em maiuscula
let palavra_1 = "xicara"
console.log(palavra_1.toUpperCase()) // XICARA

// transformando uma string maiscula em minuscula
let palavra_2 = "CAFE"
console.log(palavra_2.toLowerCase()) // cafe

// removendo uma parte de uma string
let palavra_3 = "mergulhando em cafe"
console.log(palavra_3.substring(0, 11)) // mergulhando